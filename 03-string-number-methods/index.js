/**
 * String
 * string.startsWith
 * string.endsWith
 * string.includes
 */

 let string = 'Hello, my name is Huda Prasetyo and I love JavaScript';

 console.log(string.startsWith('Hello'));
 console.log(string.endsWith('JavaScript'));
 console.log(string.includes('Huda'));
 

